# ── Plot ──────────────────────────────────────────────────────────────────────

fig, ax = plt.subplots(figsize=(18, 7))
fig.patch.set_facecolor("#0f0f1a")
ax.set_facecolor("#0f0f1a")

# Full scatter - all non-top-25 points
mask_base  = np.array([t not in top25_nums for t in throws])
mask_top25 = ~mask_base

ax.scatter(throws[mask_base],  freqs[mask_base],
           s=18, color="#4a90d9", alpha=0.55, zorder=2, linewidths=0)

ax.scatter(throws[mask_top25], freqs[mask_top25],
           s=55, color="#f5a623", alpha=1.0, zorder=4, linewidths=0)

# Vertical lines from baseline to top-25 points
for t, f in top25:
    ax.vlines(t, 0, f, colors="#f5a623", linewidth=0.8, alpha=0.4, zorder=3)

# Annotations for top 25 - smart vertical offset to avoid overlap
import heapq
annotated = sorted(top25, key=lambda x: x[0])  # sorted by throw number for layout

# Collect (x, y, label) and stagger labels
for throw_num, cnt in annotated:
    rank = next(i+1 for i, (t, _) in enumerate(top25) if t == throw_num)
    label = f"#{throw_num}\n({cnt})"
    y_offset = cnt + 0.8

    txt = ax.annotate(
        label,
        xy=(throw_num, cnt),
        xytext=(throw_num, y_offset + 0.4),
        fontsize=6.5,
        color="#f5a623",
        ha="center",
        va="bottom",
        fontweight="bold",
        arrowprops=dict(arrowstyle="-", color="#f5a623", alpha=0.4, lw=0.6),
        zorder=5,
    )
    txt.set_path_effects([pe.withStroke(linewidth=1.5, foreground="#0f0f1a")])

# The #1 spike at throw=1 needs special treatment (far left)
